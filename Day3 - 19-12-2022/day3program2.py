# Area of Cicle
radius = 10
area_of_cirlce = 3.14 * radius ** 2
print('Area of Cicle is -',area_of_cirlce)

# Area of Rectangle
length = 10
width = 20
area_of_rectangle = length * width
print('Area of Rectangle is -', area_of_rectangle)

# Weight of an object
mass = 75       # In Kg
gravity = 9.81
weight = mass * gravity
print('Weight of an object is -', weight,'N')

# Density of liquid
volume = 0.075      # In cubic meter
density = mass / volume     # 1000 Kg/m^3
print('Density of liquid is -', density)

